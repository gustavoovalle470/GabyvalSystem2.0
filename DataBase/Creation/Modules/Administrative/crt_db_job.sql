/* *****************************************************************
 * Esquema: GABYVAL
 * Tabla: ad_job
 * Descripcion: Contiene toda la informacion basica para la ejecucion
 *				de los Jobs del Scheduler del sistema
 * *****************************************************************/
 CREATE TABLE GABYVAL.ad_job(
    gb_job_id     INTEGER       NOT NULL,
    gb_job_name   VARCHAR(50)   NOT NULL,
    gb_job_class  VARCHAR(200)  NOT NULL,
    gb_job_desc   VARCHAR(200)  NOT NULL,
    gb_exp_cron   VARCHAR(50)   NOT NULL,
	gb_auto_run	  VARCHAR(1)	NOT NULL,
    create_dt     DATE          NOT NULL,
    rowversion    INTEGER       NOT NULL
);
 
