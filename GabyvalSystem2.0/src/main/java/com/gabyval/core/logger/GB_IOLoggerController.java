package com.gabyval.core.logger;

import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.File;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.util.ArrayList;

/**
 * This class permit write or read logger file.
 * @author Gustavo Ovalle
 * @version 1.2
 * @since June 23th, 2016
 */
public class GB_IOLoggerController {
    
    /**
     * This method write the file log specific, whit message specific.
     * @param f File the log file to write.
     * @param message String the message to print in the file.
     */
    public static void wirtelog(File f, String message){
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter(f));
            bw.write(message);
            bw.close();
        } catch (IOException ex) {
            
        }
    }
    
    /**
     * Read the log file.
     * @param f File the log file to read.
     * @return ArrayList All lines that content the log file.
     */
    public static ArrayList<String> loadLog(File f){
        ArrayList<String> log_lines = new ArrayList<>();
        try{
            String line;
            BufferedReader br = new BufferedReader(new FileReader(f));
            line = br.readLine();
            while(line != null){
                log_lines.add(line);
                line = br.readLine();
            }
        } catch (IOException ex) {
            
        }
        return log_lines;
    }
}