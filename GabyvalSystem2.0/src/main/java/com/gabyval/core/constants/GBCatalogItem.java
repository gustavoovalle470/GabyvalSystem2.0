package com.gabyval.core.constants;

/**
 * This class define the item for a catalog.
 * @author GAOQ
 * @version 1.0
 * @since 10/07/2017
 */
class GBCatalogItem {
    private final int itemId; // Unique id for item.
    private final String item; //Item define.
    private final String description;

    /**
     * Create a new instance for item.
     * @param itemId int the item id.
     * @param item String the item value.
     * @param description String the description for this item.
     */
    public GBCatalogItem(int itemId, String item, String description) {
        this.itemId = itemId;
        this.item = item;
        this.description = description;
    }

    /**
     * Return the item id.
     * @return int the item id.
     */
    public int getItemId() {
        return itemId;
    }

    /**
     * Return the item value.
     * @return String the item value.
     */
    public String getItem() {
        return item;
    }

    /**
     * Return the item description.
     * @return String the item description.
     */
    public String getDescription() {
        return description;
    }
}
