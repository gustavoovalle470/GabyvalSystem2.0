package com.gabyval.core.constants;

import java.util.ArrayList;

/**
 * This class contain the definition for a catalog into application.
 * @author GAOQ
 * @version 1.0
 * @since 10/07/2017.
 */
public class GBCatalog {
    private String catalog_name; //The catalog name.
    private ArrayList<GBCatalogItem> items;//The items into catalog.

    /**
     * Create a new instance for catalog.
     * @param catalog_name String the name for this catalog.
     * @param items ArrayList the items that compose this catalog.
     */
    public GBCatalog(String catalog_name, ArrayList<GBCatalogItem> items) {
        this.catalog_name = catalog_name;
        this.items = items;
    }

    /**
     * Return the catalog name.
     * @return String the catalog name.
     */
    public String getCatalog_name() {
        return catalog_name;
    }

    /**
     * Change the catalog name
     * @param catalog_name String the new catalog name.
     */
    public void setCatalog_name(String catalog_name) {
        this.catalog_name = catalog_name;
    }

    /**
     * Return the items that compose this catalog.
     * @return ArrayList the items.
     */
    public ArrayList<GBCatalogItem> getItems() {
        return items;
    }

    /**
     * Change the items that compose this catalog.
     * @param items ArrayList the new items.
     */
    public void setItems(ArrayList<GBCatalogItem> items) {
        this.items = items;
    }
}