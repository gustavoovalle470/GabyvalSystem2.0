package com.gabyval.core.constants;

import java.util.ArrayList;
import java.util.HashMap;

/**
 * This class is a conteiner for catalogs.
 * @author GAOQ
 * @version 1.0
 * @since 13/11/2017
 */
public class CatalogEntity {
    private HashMap<String, Integer> catalog; //Catalog definition.
    
    /**
     * Create a new instance of catalog.
     */
    public CatalogEntity(){
        catalog = new HashMap<>();
    }
    
    /**
     * Put values into catalog.
     * @param catalogId id for catalog item.
     * @param catalogDesc catalog item.
     */
    public void putValues(int catalogId, String catalogDesc){
        catalog.put(catalogDesc, catalogId);
    }
    
    /**
     * Return all id for the catalog items.
     * @return ArrayList ids.
     */
    public ArrayList<Integer> getIds(){
        return new ArrayList(catalog.keySet());
    }
    
    /**
     * Return all catalog items.
     * @return ArrayList all catalog items.
     */
    public ArrayList<String> getDescriptions(){
        return new ArrayList(catalog.values());
    }
    
    /**
     * Return the catalog item.
     * @param catalogItemId catalog item id.
     * @return String the catalog item.
     */
    public String decodeValue(int catalogItemId){
        for(String i: catalog.keySet()){
            if(catalog.get(i).equals(catalogItemId)){
                return i;
            }
        }
        return "N/A";
    }
    
    /**
     * Return the catalog id item.
     * @param catalogDesc String the catalog item.
     * @return int the catalog id item.
     */
    public int codeValue( String catalogDesc){
        return catalog.get(catalogDesc);
    }
    
    /**
     * Return all catalog.
     * @return HashMap all catalog.
     */
    public HashMap<String, Integer> getAllCatalog(){
        return catalog;
    }
    
    public boolean catalogExist(){
        return !catalog.isEmpty();
    }
}
